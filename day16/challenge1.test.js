import { expect } from 'chai';
import { findCheapestPath } from './challenge1.js';

describe('Day 16 Challenge 1', () => {
  it('can solve example case', () => {
    let input =
      '###############\n' +
      '#.......#....E#\n' +
      '#.......#.....#\n' +
      '#.#.###.#.###.#\n' +
      '#.....#.#...#.#\n' +
      '#.###.#####.#.#\n' +
      '#.#.#.......#.#\n' +
      '#.#.#####.###.#\n' +
      '#...........#.#\n' +
      '###.#.#####.#.#\n' +
      '#...#.....#.#.#\n' +
      '#.#.#.###.#.#.#\n' +
      '#.....#...#.#.#\n' +
      '#.###.#.#.#.#.#\n' +
      '#S..#.....#...#\n' +
      '###############';
    expect(findCheapestPath(input)).to.eq(7036);
  });

  it('can solve second example case', () => {
    let input =
      '#################\n' +
      '#...#...#...#..E#\n' +
      '#.#.#.#.#.#.#.#.#\n' +
      '#.#.#.#...#...#.#\n' +
      '#.#.#.#.###.#.#.#\n' +
      '#...#.#.#.....#.#\n' +
      '#.#.#.#.#.#####.#\n' +
      '#.#...#.#.#.....#\n' +
      '#.#.#####.#.###.#\n' +
      '#.#.#.......#...#\n' +
      '#.#.###.#####.###\n' +
      '#.#.#...#.....#.#\n' +
      '#.#.#.#####.###.#\n' +
      '#.#.#.........#.#\n' +
      '#.#.#.#########.#\n' +
      '#S#.............#\n' +
      '#################';

    expect(findCheapestPath(input)).to.eq(11048);
  });
});
